"""
7 - Faça um Programa que calcule a área de um quadrado, em seguida mostre o 
dobro desta área para o usuário.
"""
lado = float(input('Informe o valor do comprimento de um lado do quadrado: '))
area = lado*lado
print('O valor da área do quadrado é: {}'.format(area))
print('O dobro do valor da área é: {}'.format(2*area))
